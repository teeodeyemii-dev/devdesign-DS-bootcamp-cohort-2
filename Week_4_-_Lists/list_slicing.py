# List Slicing - to get a chunk/part of a list

names = ["Mike", "Jane", "Blessing", "Peter", "Joe", "Sarah", "Chris", "Daniel", "Jasper", "Irine"]

# Get names from the 3rd to 6th position in the list
# The end position should always be +1 from where we intend to end/stop
print(names[2:6])

print("All items", names[:])
print("Return items from the start of the list to the 3rd index position", names[:4])
